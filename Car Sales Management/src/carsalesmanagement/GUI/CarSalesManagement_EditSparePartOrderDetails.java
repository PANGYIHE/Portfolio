    private void jButtonupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonupdateActionPerformed
        // TODO add your handling code here:
        String UpdateQuery = null;
        PreparedStatement ps = null;
        Connection con = getConnection();

        try {
            UpdateQuery = "UPDATE ordersparepart SET sparePartID = ?, sparePartName= ?, quantity = ?, price = ? , totalPrice =? WHERE ID = ?";

            ps = con.prepareStatement(UpdateQuery);

            ps.setString(1, jTextFieldssID.getText());
            ps.setString(2, jTextFieldssName.getText());
            ps.setInt(3, Integer.parseInt(jTextFieldquantity.getText()));
            ps.setDouble(4, Double.parseDouble(jTextFieldprice.getText()));

            int a = Integer.parseInt(jTextFieldquantity.getText());

            double  b = Double.parseDouble(jTextFieldprice.getText());

            double c=a*b;

            String totalPrice = String.format("%.2f", c);

            jLabel8.setText(totalPrice);

            ps.setString(5, jLabel8.getText());

            ps.setInt(6, Integer.parseInt(jTextFieldID.getText()));

            ps.executeUpdate();
            Show_Data_In_Table(jTableData);
            JOptionPane.showMessageDialog(null,"Data successfully updated");

        } catch (SQLException ex) {
            Logger.getLogger(CarSalesManagement_EditSparePartOrderDetails.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonupdateActionPerformed

